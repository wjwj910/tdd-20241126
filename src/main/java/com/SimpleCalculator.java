package com;

public class SimpleCalculator {
    public int multiply(int num1, int num2) {
        return num1 * num2;
    }

    public int minus(int num1, int num2) {
        return num1 - num2;
    }

    public int divide(int num1, int num2) {
        return num1 / num2;
    }


    public int reminder(int num1, int num2) {
        return num1 % num2;
    }
}
